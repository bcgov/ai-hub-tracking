/**
 * Builds the `openai` HCL block for a given environment.
 *
 * When enabled, includes model deployment entries and, in `test` environments, diagnostics.
 *
 * @param enabled - Whether OpenAI is enabled for this tenant.
 * @param modelDeployments - The list of model deployment configurations to embed.
 * @param env - The target environment name used to conditionally add diagnostics.
 * @returns The `openai = { ... }` HCL block as a string.
 */
function openAiBlock(
  enabled: boolean,
  modelDeployments: ModelDeployment[],
  env: EnvironmentName,
): string {
  const lines = [
    '  openai = {',
    `    enabled = ${enabled ? 'true' : 'false'}`,
    '    sku     = "S0"',
  ];

  if (env === 'test') {
    lines.push(
      '    diagnostics = {',
      '      log_groups        = ["allLogs"]',
      '      log_categories    = []',
      '      metric_categories = ["AllMetrics"]',
      '    }',
    );
  }

  if (enabled && modelDeployments.length > 0) {
    lines.push('    model_deployments = [');
    for (const deployment of modelDeployments) {
      lines.push(
        '      {',
        `        name          = "${deployment.name}"`,
        `        model_name    = "${deployment.model_name}"`,
        `        model_version = "${deployment.model_version}"`,
        `        scale_type    = "${deployment.scale_type}"`,
        `        capacity      = ${deployment.capacity}`,
        '      },',
      );
    }
    lines.push('    ]');
  }

  lines.push('  }');
  return lines.join('\n');
}

/**
 * Builds the `user_management` HCL block including all seed member role assignments.
 *
 * Returns an empty `user_management = {}` block when no users are specified in
 * any of the admin, write, or read role lists.
 *
 * @param data - The validated tenant form submission containing user role lists.
 * @returns The `user_management = { ... }` HCL block as a string.
 */
function buildUserManagement(data: TenantFormData): string {
  const adminUsers = data.admin_users ?? [];
  const writeUsers = data.write_users ?? [];
  const readUsers = data.read_users ?? [];
  const hasUsers = adminUsers.length > 0 || writeUsers.length > 0 || readUsers.length > 0;

  if (!hasUsers) {
    return '  user_management = {}';
  }

  const roleBlocks = [
    buildRoleBlock('admin', adminUsers),
    buildRoleBlock('write', writeUsers),
    buildRoleBlock('read', readUsers),
  ].filter(Boolean);

  return ['  user_management = {', '    seed_members = {', ...roleBlocks, '    }', '  }'].join(
    '\n',
  );
}

/**
 * Builds a single HCL role block listing the email addresses assigned to that role.
 *
 * Returns `null` when the email list is empty so the caller can filter out absent roles.
 *
 * @param role - The role name (e.g. `'admin'`, `'write'`, `'read'`).
 * @param emails - The list of email addresses to assign to the role.
 * @returns An HCL snippet for the role block, or `null` if no emails are provided.
 */
function buildRoleBlock(role: string, emails: string[]): string | null {
  if (emails.length === 0) {
    return null;
  }

  return [
    `      ${role} = [`,
    emails.map((email) => `        "${email}"`).join(',\n'),
    '      ]',
  ].join('\n');
}

