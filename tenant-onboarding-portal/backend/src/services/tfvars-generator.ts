/**
 * Derives the list of model deployment configurations from the selected model families
 * and capacity tier.
 *
 * Each model within each selected family is scaled by the tier multiplier, with a
 * minimum capacity of 1.
 *
 * @param families - The list of model family keys selected by the tenant (e.g. `['gpt4o']`).
 * @param capacityTier - The capacity tier key (e.g. `'standard'`, `'high'`).
 * @returns An array of deployment configuration objects ready for HCL serialisation.
 */
function buildModelDeployments(families: string[], capacityTier: string): ModelDeployment[] {
  const multiplier =
    CAPACITY_TIERS[capacityTier as keyof typeof CAPACITY_TIERS]?.multiplier ??
    CAPACITY_TIERS.standard.multiplier;
  const deployments: ModelDeployment[] = [];

  for (const familyKey of families) {
    const family = MODEL_FAMILIES[familyKey as keyof typeof MODEL_FAMILIES];
    if (!family) {
      continue;
    }

    for (const model of family.models) {
      deployments.push({
        name: model.name,
        model_name: model.model_name,
        model_version: model.model_version,
        scale_type: model.scale_type,
        capacity: Math.max(1, Math.trunc(model.default_capacity * multiplier)),
      });
    }
  }

  return deployments;
}
