    lines = [
        f"  document_intelligence = {{",
        f"    enabled = {'true' if enabled else 'false'}",
        f'    sku     = "S0"',
        f'    kind    = "FormRecognizer"',
    ]
    if env == "test":
        lines += [
            f"    diagnostics = {{",
            f'      log_groups        = ["allLogs"]',
            f"      log_categories    = []",
            f'      metric_categories = ["AllMetrics"]',
            f"    }}",
        ]
