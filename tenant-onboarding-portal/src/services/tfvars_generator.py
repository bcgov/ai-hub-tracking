def _log_analytics_block(defaults: dict) -> str:
    return "\n".join([
        f"  log_analytics = {{",
        f"    enabled        = true",
        f"    retention_days = {defaults['log_retention_days']}",
        f'    sku            = "PerGB2018"',
        f"  }}",
    ])


def _openai_block(enabled: bool, model_deployments: list[dict], env: str) -> str:
    lines = [
        f"  openai = {{",
        f"    enabled = {'true' if enabled else 'false'}",
        f'    sku     = "S0"',
    ]
    if env == "test":
        lines += [
            f"    diagnostics = {{",
            f'      log_groups        = ["allLogs"]',
            f"      log_categories    = []",
            f'      metric_categories = ["AllMetrics"]',
            f"    }}",
        ]

    if enabled and model_deployments:
        lines.append(f"    model_deployments = [")
        for i, m in enumerate(model_deployments):
            lines += [
                f"      {{",
                f'        name          = "{m["name"]}"',
                f'        model_name    = "{m["model_name"]}"',
                f'        model_version = "{m["model_version"]}"',
                f'        scale_type    = "{m["scale_type"]}"',
                f'        capacity      = {m["capacity"]}',
                f"      }},",
            ]
        lines.append(f"    ]")
    lines.append(f"  }}")
    return "\n".join(lines)


def _apim_auth_block(auth_mode: str) -> str:
    return "\n".join([
        f"  apim_auth = {{",
        f'    mode                 = "{auth_mode}"',
        f"    key_rotation_enabled = false",
        f"  }}",
    ])


def _build_user_management(admin_emails: list[str]) -> str:
    if not admin_emails:
        return "  user_management = {}"

    email_lines = ",\n".join(f'        "{e}"' for e in admin_emails)
    return "\n".join([
        f"  user_management = {{",
        f"    seed_members = {{",
        f"      admin = [",
        f"{email_lines}",
        f"      ]",
        f"    }}",
        f"  }}",
    ])


def _apim_policies_block(data: TenantFormData) -> str:
    return "\n".join([
        f"  apim_policies = {{",
        f"    rate_limiting = {{",
        f"      enabled           = {'true' if data.rate_limiting_enabled else 'false'}",
        f"      tokens_per_minute = {data.tokens_per_minute}",
        f"    }}",
        f"    pii_redaction = {{",
        f"      enabled     = {'true' if data.pii_redaction_enabled else 'false'}",
        f"      fail_closed = false",
        f"    }}",
        f"    usage_logging = {{",
        f"      enabled = {'true' if data.usage_logging_enabled else 'false'}",
        f"    }}",
        f"    streaming_metrics = {{",
        f"      enabled = true",
        f"    }}",
        f"    tracking_dimensions = {{",
        f"      enabled = true",
        f"    }}",
        f"    intelligent_routing = {{",
        f"      enabled = false",
        f"    }}",
        f"  }}",
    ])


def _build_model_deployments(families: list[str], capacity_tier: str) -> list[dict]:
    """Expand selected model families into individual deployment entries."""
    multiplier = CAPACITY_TIERS.get(capacity_tier, CAPACITY_TIERS["standard"])["multiplier"]
    deployments = []
    for family_key in families:
        family = MODEL_FAMILIES.get(family_key)
        if not family:
            continue
        for model in family["models"]:
            deployments.append({
                "name": model["name"],
                "model_name": model["model_name"],
                "model_version": model["model_version"],
                "scale_type": model["scale_type"],
                "capacity": max(1, int(model["default_capacity"] * multiplier)),
            })
    return deployments
